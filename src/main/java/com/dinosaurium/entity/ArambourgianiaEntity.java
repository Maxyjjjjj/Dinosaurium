	@Override
	protected void registerGoals() {
		super.registerGoals();
		this.goalSelector.addGoal(1, new BreedGoal(this, 1));
		this.goalSelector.addGoal(2, new FollowParentGoal(this, 0.8));
		this.goalSelector.addGoal(3, new MeleeAttackGoal(this, 1.2, false) {
			@Override
			protected boolean canPerformAttack(LivingEntity entity) {
				return this.isTimeToAttack() && this.mob.distanceToSqr(entity) < (this.mob.getBbWidth() * this.mob.getBbWidth() + entity.getBbWidth()) && this.mob.getSensing().hasLineOfSight(entity);
			}
		});
		this.goalSelector.addGoal(4, new Goal() {
			{
				this.setFlags(EnumSet.of(Goal.Flag.MOVE));
			}

			public boolean canUse() {
				if (ArambourgianiaEntity.this.getTarget() != null && !ArambourgianiaEntity.this.getMoveControl().hasWanted()) {
					return true;
				} else {
					return false;
				}
			}

			@Override
			public boolean canContinueToUse() {
				return ArambourgianiaEntity.this.getMoveControl().hasWanted() && ArambourgianiaEntity.this.getTarget() != null && ArambourgianiaEntity.this.getTarget().isAlive();
			}

			@Override
			public void start() {
				LivingEntity livingentity = ArambourgianiaEntity.this.getTarget();
				Vec3 vec3d = livingentity.getEyePosition(1);
				ArambourgianiaEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
			}

			@Override
			public void tick() {
				LivingEntity livingentity = ArambourgianiaEntity.this.getTarget();
				if (ArambourgianiaEntity.this.getBoundingBox().intersects(livingentity.getBoundingBox())) {
					ArambourgianiaEntity.this.doHurtTarget(livingentity);
				} else {
					double d0 = ArambourgianiaEntity.this.distanceToSqr(livingentity);
					if (d0 < 16) {
						Vec3 vec3d = livingentity.getEyePosition(1);
						ArambourgianiaEntity.this.moveControl.setWantedPosition(vec3d.x, vec3d.y, vec3d.z, 1);
					}
				}
			}
		});
		this.goalSelector.addGoal(5, new RandomStrollGoal(this, 1));
		this.goalSelector.addGoal(6, new RandomStrollGoal(this, 2, 20) {
			@Override
			protected Vec3 getPosition() {
				RandomSource random = ArambourgianiaEntity.this.getRandom();
				double dir_x = ArambourgianiaEntity.this.getX() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_y = ArambourgianiaEntity.this.getY() + ((random.nextFloat() * 2 - 1) * 16);
				double dir_z = ArambourgianiaEntity.this.getZ() + ((random.nextFloat() * 2 - 1) * 16);
				return new Vec3(dir_x, dir_y, dir_z);
			}
		});
		this.targetSelector.addGoal(7, new NearestAttackableTargetGoal(this, Pufferfish.class, false, false));
		this.targetSelector.addGoal(8, new NearestAttackableTargetGoal(this, Bat.class, false, false));
		this.targetSelector.addGoal(9, new NearestAttackableTargetGoal(this, Drowned.class, false, false));
		this.targetSelector.addGoal(10, new NearestAttackableTargetGoal(this, Cod.class, false, false));
		this.targetSelector.addGoal(11, new NearestAttackableTargetGoal(this, Rabbit.class, false, false));
		this.targetSelector.addGoal(12, new NearestAttackableTargetGoal(this, Salmon.class, false, false));
		this.targetSelector.addGoal(13, new HurtByTargetGoal(this));
		this.goalSelector.addGoal(14, new RandomLookAroundGoal(this));
		this.goalSelector.addGoal(15, new FloatGoal(this));
	}

